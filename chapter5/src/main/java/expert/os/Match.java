package expert.os;

public class Math {

    int sum(int a, int b) {
        return a + b;
    }

    static int multiply(int a, int b) {
        return a * b;
    }

    double subtract(double a, int b) {
        return a - b;
    }

    static double divide(double a, long b) {
        return a + b;
    }
}
